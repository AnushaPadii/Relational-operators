public class Relational {
    public static void main(String[] args) {
        int a=56;
        int b=40;
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a>=b);
        System.out.println(a<=b);
        System.out.println(a!=b);
        System.out.println(a==b);
        
    }
}
